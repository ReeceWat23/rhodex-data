/** Build quarterly series for QuarterlyTracker: { year, quarter, loss_B, events } */
export const QUARTERLY_DATA = (() => {
  const out = [];
  for (let y = 2000; y <= 2024; y++) {
    for (const q of [1, 2, 3, 4]) {
      const d = Q_HIST[q];
      const idx = y - 2000;
      out.push({
        year: y,
        quarter: q,
        loss_B: d.vals[idx] ?? 0,
        events: d.lam ?? 90,
      });
    }
  }
  return out;
})();

